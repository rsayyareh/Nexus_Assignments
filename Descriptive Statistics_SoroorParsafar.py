# ğŸ“¦ Import Required Libraries
import numpy as np
from scipy import stats
import matplotlib.pyplot as plt

# ğŸ² Step 1: Generate Random Integer List
np.random.seed(42)  # For reproducibility
data = np.random.randint(20, 40, size=30)
print("Generated Data:", data)

"""### ğŸ“Š Step 2: Central Tendency and Quantiles
Let's compute basic statistics â€” mean, median, mode, and quantiles.
"""

# ğŸ“ Central Tendency
# mean
mean_val = np.mean(data)
# median
median_val = np.median(data)
# mode
mode_val = stats.mode(data, keepdims=True).mode[0]

# ğŸ“ Quantiles
q1 = np.quantile(data, 0.25)
q3 = np.quantile(data, 0.75)

print(f"Mean: {mean_val}")
print(f"Median: {median_val}")
print(f"Mode: {mode_val}")
print(f"Q1 (25%): {q1}")
print(f"Q3 (75%): {q3}")

"""### ğŸ§ª Step 3: Skewness and Kurtosis
Skewness shows asymmetry, and kurtosis indicates the "tailedness" of the distribution.
"""

# ğŸ“‰ Skewness and Kurtosis
skew_val = stats.skew(data)
kurtosis_val = stats.kurtosis(data)

print(f"Skewness: {skew_val:.2f}")
print(f"Kurtosis: {kurtosis_val:.2f}")

"""### ğŸ“ˆ Step 4: Visualization - Bar Chart and Boxplot
Visualize the data to better understand its distribution and spread.
"""

# ğŸ“Š Bar Plot
plt.figure(figsize=(12, 4))

plt.subplot(1, 2, 1)
# make a bar chart
plt.bar(range(len(data)), data)
plt.title("Bar Plot of Random Data")
plt.xlabel("Index")
plt.ylabel("Value")

# ğŸ“¦ Boxplot
plt.subplot(1, 2, 2)
# make a box plot
plt.boxplot(data, vert=False)
plt.title("Boxplot of Random Data")
plt.xlabel("Value")

plt.tight_layout()
plt.show()