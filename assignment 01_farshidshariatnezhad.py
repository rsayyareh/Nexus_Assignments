greet("bob", times=3)   # Bob Bob Bob

class Counter:
    """Counts how many times `increment` is called."""

    def __init__(self):
        # 1. شمارنده داخلی را با مقدار 0 مقداردهی اولیه می‌کنیم
        self.count = 0

    def increment(self, step: int = 1):
        # 2. به شمارنده مقدار step را اضافه می‌کنیم
        self.count += step

    def value(self):
        # 3. مقدار فعلی شمارنده را برمی‌گردانیم
        return self.count

# تست کد:
c = Counter()
for _ in range(5):
    c.increment()
print(c.value())   # Expected: 5

import math

class Point:
    """
    A 2-D point supporting distance calculation.
    Usage:
        p = Point(3, 4)
        q = Point(0, 0)
        print(p.distance_to(q))  # 5.0
    """

    def __init__(self, x: float, y: float):
        # 1. ذخیره مختصات x و y به عنوان ویژگی‌ها
        self.x = x
        self.y = y

    def distance_to(self, other: 'Point') -> float:
        # 2. محاسبه فاصله با استفاده از فرمول اقلیدسی
        return math.sqrt((self.x - other.x) ** 2 + (self.y - other.y) ** 2)

# Smoke test
