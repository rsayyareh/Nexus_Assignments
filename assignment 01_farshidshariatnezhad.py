import math

def is_prime(n: int) -> bool:
    """
    Return True if n is a prime number, else False.
    0 and 1 are *not* prime.
    """
    # 0 و 1 اعداد اول نیستند
    if n <= 1:
        return False

    # چک کردن تقسیم‌پذیری از 2 تا √n
    for i in range(2, int(math.sqrt(n)) + 1):
        if n % i == 0:
            return False  # اگر به عددی غیر از 1 و خود n تقسیم شد، عدد اول نیست

    return True  # اگر هیچ تقسیم‌پذیری پیدا نشد، عدد اول است

# Quick self-check
print([x for x in range(10) if is_prime(x)])   # Expected: [2, 3, 5, 7]

def greet(name: str, times: int = 1) -> None:
    """Print `name`, capitalised, exactly `times` times on one line."""
    # تبدیل نام به حرف بزرگ و چاپ آن times بار
    print(" ".join([name.capitalize()] * times))

# تست کد:
