import math

class Point:
    def __init__(self, x, y):
        self.x = x
        self.y = y

    def distance_to(self, other):
        dx = self.x - other.x
        dy = self.y - other.y
        return math.sqrt(dx**2 + dy**2)

p = Point(6, 10)
q = Point(0, 0)
assert round(p.distance_to(q), 1)
print(round(p.distance_to(q), 1))